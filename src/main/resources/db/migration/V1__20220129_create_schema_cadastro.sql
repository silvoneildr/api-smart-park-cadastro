    DATA_NASCIMENTO TIMESTAMP NULL,
    DATA_CRIACAO TIMESTAMP NOT NULL DEFAULT NOW(),
    DATA_ALTERACAO TIMESTAMP NOT NULL DEFAULT NOW(),
    CONSTRAINT PK_PESSSOA_FISICA PRIMARY KEY (PESSOA_FISICA_ID)
);
COMMENT ON TABLE CADASTRO.PESSOA_FISICA IS 'Armazena dados de pessoa física';
COMMENT ON COLUMN CADASTRO.PESSOA_FISICA.PESSOA_FISICA_ID IS 'Identificador único de cada registro';
COMMENT ON COLUMN CADASTRO.PESSOA_FISICA.PESSOA_ID IS 'Identificador único de pessoa';
