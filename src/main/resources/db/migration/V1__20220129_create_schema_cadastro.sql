CREATE SCHEMA IF NOT EXISTS CADASTRO;

CREATE TABLE CADASTRO.PESSOA (
    PESSOA_ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    TIPO VARCHAR(1) NOT NULL,
    DATA_CRIACAO TIMESTAMP NOT NULL DEFAULT NOW(),
    DATA_ALTERACAO TIMESTAMP NOT NULL DEFAULT NOW(),
    CONSTRAINT PK_PESSSOA PRIMARY KEY (PESSOA_ID)
);
COMMENT ON TABLE CADASTRO.PESSOA IS 'Armazena dados de pessoa';
COMMENT ON COLUMN CADASTRO.PESSOA.PESSOA_ID IS 'Identificador único de pessoa';
COMMENT ON COLUMN CADASTRO.PESSOA.TIPO IS 'Tipo de pessoa deve ser F - física ou J - jurídica.';
COMMENT ON COLUMN CADASTRO.PESSOA.DATA_CRIACAO IS 'Data de criação.';
COMMENT ON COLUMN CADASTRO.PESSOA.DATA_ALTERACAO IS 'Data de atualização.';

CREATE TABLE CADASTRO.PESSOA_FISICA (
