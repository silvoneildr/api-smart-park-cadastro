COMMENT ON COLUMN CADASTRO.PESSOA_FISICA.CPF IS 'Número de CPF válido';
COMMENT ON COLUMN CADASTRO.PESSOA_FISICA.RG IS 'Registro geral de algum estado brasileiro';
COMMENT ON COLUMN CADASTRO.PESSOA_FISICA.CNH IS 'Carteira nacional de habilitação, padrão brasileiro';
